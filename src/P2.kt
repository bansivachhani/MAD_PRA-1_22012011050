fun main() {
    var a: Int = 4
    println("Integer value: $a")
    var b: Double = a.toDouble()
    println("Double value from Int: $b")
    var c: String = "10"
    println("String value: $c")
    var d: Int = c.toInt()
    println("Integer value from String: $d")
    var e: Double = c.toDouble()
    println("Double value from String: $e")}